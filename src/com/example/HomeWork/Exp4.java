package com.example.HomeWork;

public class Exp4 {
    public static void main(String[] args) {
        System.out.println("Even numbers");
        for(int i=0;i<=30;i++){
            if(i%2==0){
                System.out.print(i+" ");
            }
        }
        System.out.println();
        System.out.println("Odd numbers");
        for(int i=0;i<=30;i++){
            if(i%2!=0){
                System.out.print(i+" ");
            }
        }

    }
}
