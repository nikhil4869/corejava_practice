package com.example.Operatars;

public class PreAndPosIncDec {
    public static void main(String[] args) {
        int a=10;
        int b=a++;
        int c=++b;
        int d=--a;
        int e= d--;
        System.out.println("print a value" + a);
        System.out.println("print b value" + b);
        System.out.println("print c value" + c);
        System.out.println("print d value" + d);
        System.out.println("print e value" + e);

    }
}
