package com.example.Operatars;

public class AssignmentExp {
    public static void main(String[] args) {
        int i=10;
        i+=10;
        System.out.println(i);
        int j =20;
        j-=5;
        System.out.println(j);
        int k =2;
        k*=5;
        System.out.println(k);
        int m=10;
        m/=2;
        System.out.println(m);
    }
}
