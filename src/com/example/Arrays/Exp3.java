package com.example.Arrays;

public class Exp3 {
    public static void main(String[] args) {
        String[] fruits = new String[3];
        fruits[0]="apple";
        fruits[1]="Banana";
        fruits[2]="Mango";
        int i=0;
        while (i< fruits.length){
            System.out.println(fruits[i]);
            i++;
        }

    }
}
